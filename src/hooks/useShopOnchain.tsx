const CONTRACT_ADDRESS = "YOUR_CONTRACT_ADDRESS_HERE";

// Helper type for store info
type StoreInfo = {
  storeAddress: string;
  owner: string;
  name: string;
  creationTime: number;
};

export function useCreateStore() {
  const { config } = usePrepareContractWrite({
    address: CONTRACT_ADDRESS,
    abi: merchStoreFactoryABI,
    functionName: "createStore",
  });
  return useWriteContract(config);
}

export function useGetStoreInfo(storeId: number) {
  return useContractRead({
    address: CONTRACT_ADDRESS,
    abi: merchStoreFactoryABI,
    functionName: "getStoreInfo",
    args: [storeId],
  }) as { data: StoreInfo | undefined; isError: boolean; isLoading: boolean };
}

